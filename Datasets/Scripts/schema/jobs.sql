-- 1. JOBS Table
CREATE TABLE JOBS (
    JOB_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    JOB_TITLE     VARCHAR2(100) NOT NULL,
    DEPARTMENT    VARCHAR2(50),
    POSTED_DATE   DATE DEFAULT SYSDATE,
    SALARY        NUMBER(10,2)
);
-- Index on JOB_TITLE for faster searches
CREATE INDEX IDX_JOB_TITLE ON JOBS(JOB_TITLE);
CREATE TABLE job_applications (
    application_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    applicant_id NUMBER NOT NULL,
    job_id NUMBER NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING',
    apply_date DATE DEFAULT SYSDATE,
    CONSTRAINT fk_applicant FOREIGN KEY(applicant_id) REFERENCES applicants(applicant_id),
    CONSTRAINT fk_job FOREIGN KEY(job_id) REFERENCES jobs(job_id),
    CONSTRAINT chk_status CHECK(status IN ('PENDING', 'APPROVED', 'REJECTED'))
);
